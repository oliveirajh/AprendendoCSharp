namespace OperadoresAtrubuicao
{
    class Program{
        static void Main(string[] args){

            int a = 10;
            a+=2;
            System.Console.WriteLine(a);
            a*=3;
            System.Console.WriteLine(a);

            string s = "ABC";
            System.Console.WriteLine(s);
            s+="DEF"; //Concatena String
            System.Console.WriteLine(s);

            a++ //padrão de todas as linguagens
            System.Console.WriteLine(a);
            a--
            System.Console.WriteLine(a);
        }
    }
}